    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        if (tVeiculo.getSelectedRow() != -1) {
            int id = (int) tVeiculo.getValueAt(tVeiculo.getSelectedRow(), 0);
            String modelo = txtModelo.getText();
            String ano = txtAno.getText();
            String chassis = txtChassis.getText();
            String valorDiaria = txtVDiaria.getText();
            String capacidade = txtCapacidade.getText();
            String combustivel = txtCombustivel.getText();
            String marca = txtMarca.getText();  

            Veiculo veiculo = new Veiculo(modelo, ano, chassis, valorDiaria, capacidade,combustivel, marca, id);
            VeiculoDAO c = new VeiculoDAO();

            c.deletar(veiculo);

            txtModelo.setText("");
            txtAno.setText("");
            txtChassis.setText("");
            txtVDiaria.setText("");
            txtCapacidade.setText("");
            txtCombustivel.setText("");
            txtMarca.setText("");

            readJTable();
        }
    }//GEN-LAST:event_botaoExcluirActionPerformed

