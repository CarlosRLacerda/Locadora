    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) tVeiculo.getModel();
        modelo.setNumRows(0);
        VeiculoDAO vdao = new VeiculoDAO();

        for (Veiculo v : vdao.read()) {

            modelo.addRow(new Object[]{
                v.getId(),
                v.getModelo(),
                v.getAno(),
                v.getChassis(),
                v.getValorDiaria(),
                v.getCapacidade(),
                v.getCombustivel(),
                v.getMarca()             
                
            });

        }

    }
    
